// Afficher l'écran
//--------------------------------------------------------------------------------------------------
async function AfficherEcranPrincipal()
{
  SpeechStop();
  if (!gModeSimulation)
    openFullscreen();
  if (IsNotInstalled())
  {
    if (gVoixInterface) Speech("écran principal");
    AfficherEcran("EcranPrincipal");
  }
}

